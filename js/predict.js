function getScore(a, b, matrix) {
    a = a.toUpperCase();
    b = b.toUpperCase();
    const idxA = aminoAcidMap[a];
    const idxB = aminoAcidMap[b];
    if (idxA === undefined || idxB === undefined) {
        return -1; // 
    }
    return matrix[idxA][idxB];
}

